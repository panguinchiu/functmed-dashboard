export function MobileNav({ role }: MobileNavProps) {
  const pathname = usePathname()
  const items = navItems.filter(item => !item.adminOnly || role === 'CLINIC_ADMIN')

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-40 glass border-t border-white/60 flex">
      {items.map(item => {
        const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex-1 flex flex-col items-center justify-center gap-1 py-2.5 text-[10px] font-medium transition-colors ${
              isActive ? 'text-amber-600' : 'text-gray-400'
            }`}
          >
            <span className={isActive ? 'text-amber-500' : ''}>{item.icon}</span>
            {item.label}
          </Link>
        )
      })}
    </nav>
  )
}
